////////////////////////////////////////////////////////////////////////////////

function min(x, y) { //Add 2 different parameters that can be numbers
  //Determine if x is less than y
  if (x < y){
    //Return x
    return x;
  } else { //Else if x is greater than y
    //Return y
    return y;
  }
}

